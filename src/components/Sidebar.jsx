export default function Sidebar() {
  const { profile, isGuest, canAccess, logout } = useAuth()
  const isAdmin = profile?.role === 'admin'

  const visibleNav = NAV.filter(item => {
    if (item.adminOnly) return isAdmin
    if (isGuest) return true           // guests see all nav items (but no data)
    if (!item.page) return true        // no page key = always visible
    return canAccess(item.page)
  })

  return (
    <aside className="w-60 min-h-screen bg-white border-r border-gray-200 flex flex-col">
      <div className="px-6 py-5 border-b border-gray-100">
        <div className="text-brand-700 font-bold text-lg leading-tight">NxtWave</div>
        <div className="text-gray-500 text-xs mt-0.5">Edge Program</div>
      </div>

      <nav className="flex-1 px-3 py-4 space-y-0.5">
        {visibleNav.map(({ to, label, icon }) => (
          <NavLink
            key={to}
            to={to}
            end={to === '/'}
            className={({ isActive }) =>
              `flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-brand-50 text-brand-700'
                  : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
              }`
            }
          >
            <svg className="w-5 h-5 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d={icon} />
            </svg>
            {label}
          </NavLink>
        ))}
      </nav>

      <div className="px-4 py-4 border-t border-gray-100">
        <div className="flex items-center gap-3 mb-3">
          <div className="w-8 h-8 rounded-full bg-brand-100 flex items-center justify-center text-brand-700 font-semibold text-sm">
            {isGuest ? '?' : (profile?.name?.[0]?.toUpperCase() ?? '?')}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-gray-900 truncate">{isGuest ? 'Guest' : (profile?.name ?? 'User')}</p>
            <p className="text-xs text-gray-500 capitalize">{isGuest ? 'Read-only' : (profile?.role?.replace('_', ' ') ?? '')}</p>
          </div>
        </div>
        <button
          onClick={logout}
          className="w-full text-left text-xs text-gray-500 hover:text-red-600 transition-colors px-1"
        >
          Sign out
        </button>
      </div>
    </aside>
  )
}
